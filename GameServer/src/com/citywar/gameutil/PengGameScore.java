package com.citywar.gameutil;



public class PengGameScore {
	
	private String name = "";
    
	private int score = 0;
	
	public PengGameScore() {  
    }  
	
	public String getName()
	{
		return this.name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public int getScore()
	{
		return this.score;
	}
	
	public void setScore(int score)
	{
		this.score = score;
	}
	
}
